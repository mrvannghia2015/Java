    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
       //Thêm vào vector NhanVien
       
       
       NhanVien hangmoi = new NhanVien();
       hangmoi.setManv(txtmanv.getText());
       hangmoi.setPx(boxpx.getSelectedItem().toString());
       hangmoi.setSosp(Integer.parseInt(txtsosp.getText()));
       nv.add(hangmoi);
       
       //Thêm vào table
        Vector<String> hang = new Vector<String>();
        hang.add(hangmoi.getManv());
        hang.add(hangmoi.getPx());
        hang.add(txtsosp.getText());
        if(hangmoi.VuotChuan() == true ) hang.add("X");
        else hang.add("");
        ds.addRow(hang);
        tblnhanvien.setModel(ds);
        
        
        txtmanv.setText("");
        txtsosp.setText("");
        txtluong.setText("");
        txtsospchuan.setText("");
    }//GEN-LAST:event_btnthemActionPerformed

