    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        int kt = JOptionPane.showConfirmDialog(null,"Bạn có muốn xóa",
                                                    "Thông báo",
                                                    JOptionPane.YES_NO_OPTION);
        if(kt == JOptionPane.YES_OPTION)
        {
            int i = tblnhanvien.getSelectedRow();
            if(i>-1)
            {
            nv.removeElementAt(i);
            ds.removeRow(i);
            tblnhanvien.setModel(ds);         
            }
        }
    }//GEN-LAST:event_btnxoaActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        int i =  tblnhanvien.getSelectedRow();
        NhanVien dsNV = new NhanVien();
        dsNV.setManv(txtmanv.getText());
        dsNV.setPx(boxpx.getSelectedItem().toString());
        dsNV.setSosp(Integer.parseInt(txtsosp.getText()));
        nv.setElementAt(dsNV, i);
        ds.setValueAt(dsNV.getManv(), i, 0);
        ds.setValueAt(dsNV.getPx(),i,1);
        ds.setValueAt(dsNV.getSosp(),i,2);
        if(dsNV.VuotChuan() == true ) ds.setValueAt("X",i,3);
        else ds.setValueAt("",i,3);
        
    }//GEN-LAST:event_btnsuaActionPerformed

